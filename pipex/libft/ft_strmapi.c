#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*dest;
	size_t	i;

	dest = ft_calloc(ft_strlen(s) + 1, sizeof(char));
	if (!dest || !s || !f)
	{
		return (NULL);
	}
	i = 0;
	while (s[i])
	{
		dest[i] = f(i, s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
